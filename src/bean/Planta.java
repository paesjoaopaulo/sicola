package bean;

public class Planta extends Bean {

    private int id;
    private int rua;
    private int numero;
    private String data_plantio;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getRua() {
        return rua;
    }

    public void setRua(int rua) {
        this.rua = rua;
    }

    public int getNumero() {
        return numero;
    }

    public void setNumero(int numero) {
        this.numero = numero;
    }

    public String getData_plantio() {
        return data_plantio;
    }

    public void setData_plantio(String data_plantio) {
        this.data_plantio = data_plantio;
    }
    
    

}
